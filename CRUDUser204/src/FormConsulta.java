    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        //1.obtener el valor de txt
        String idtext = txrId.getText();
        //2. validar txt vacio
        if(idtext.isEmpty()){
            JOptionPane.showMessageDialog(this, "El Id es obligatorrio", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        //3.Busqueda, llenado de la tabla
        try{
        int id= Integer.parseInt(idtext);
        ResultSet rs= crud.buscarPorId(id);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        
        if(rs.next()){
            modelo.addRow(new Object[]{rs.getInt("id"),rs.getString("bombre"),rs.getString("correo"),rs.getString("contrasena")});
        }
    }
        catch(SQLException e){
            System.out.println("Error al llenar la tabla: "+ e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

